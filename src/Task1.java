public class Task1 {
    public static void main(String[] args) {
        float a = 10;
        float b = 20;
        System.out.println(" Якщо а=10, b=20");
// Вирахувати суму
        float c = a + b;
        System.out.println("Сума (a+b) = " + c);
// Вирахувати різницю
        float d = a - b;
        System.out.println("Різниця (a-b) = " + d);
// Вирахувати добуток
        float e = a * b;
        System.out.println("Добуток (a*b) = " + e);
// Вирахувати частку
        float f = a / b;
        System.out.println("Частка (a/b) = " + f);
    }
}
